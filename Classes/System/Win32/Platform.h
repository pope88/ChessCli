#ifndef SYSTEM_PLATFORMWIN32_H
#define SYSTEM_PLATFORMWIN32_H

////////////////////////////////////////////////////////////
// Headers
////////////////////////////////////////////////////////////
#include <Config.h>
#include <vector>


namespace System
{
namespace priv
{
////////////////////////////////////////////////////////////
/// \brief Gives access to some system-specific low-level functions
///
////////////////////////////////////////////////////////////
class Platform
{
public :

    ////////////////////////////////////////////////////////////
    /// \brief Get the current system time
    ///
    /// \return System time, in seconds
    ///
    ////////////////////////////////////////////////////////////
    static double GetSystemTime();

    ////////////////////////////////////////////////////////////
    /// \brief Suspend the execution of the current thread for a specified time
    ///
    /// \param time Time to sleep, in seconds
    ///
    ////////////////////////////////////////////////////////////
    static void Sleep(UInt32 time);
};

} // namespace priv

} // namespace System


#endif // SYSTEM_PLATFORMWIN32_H
