#include <System/Win32/ThreadLocalImpl.h>


namespace System
{
namespace priv
{
////////////////////////////////////////////////////////////
ThreadLocalImpl::ThreadLocalImpl()
{
    myIndex = TlsAlloc();
}


////////////////////////////////////////////////////////////
ThreadLocalImpl::~ThreadLocalImpl()
{
    TlsFree(myIndex);
}


////////////////////////////////////////////////////////////
void ThreadLocalImpl::SetValue(void* value)
{
    TlsSetValue(myIndex, value);
}


////////////////////////////////////////////////////////////
void* ThreadLocalImpl::GetValue() const
{
    return TlsGetValue(myIndex);
}

} // namespace priv

} // namespace System
