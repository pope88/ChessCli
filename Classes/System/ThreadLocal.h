/// \class System::ThreadLocal
/// \ingroup system
///
/// This class manipulates void* parameters and thus is not
/// appropriate for strongly-typed variables. You should rather
/// use the System::ThreadLocalPtr template class.
///
////////////////////////////////////////////////////////////
