#include <System/Clock.h>
#include <System/Platform.h>


namespace System
{
////////////////////////////////////////////////////////////
Clock::Clock()
{
    Reset();
}


////////////////////////////////////////////////////////////
float Clock::GetElapsedTime() const
{
    return static_cast<float>(priv::Platform::GetSystemTime() - myStartTime);
}


////////////////////////////////////////////////////////////
void Clock::Reset()
{
    myStartTime = priv::Platform::GetSystemTime();
}

} // namespace System
